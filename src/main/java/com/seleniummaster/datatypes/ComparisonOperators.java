package com.seleniummaster.datatypes;

public class ComparisonOperators {
    public static void main(String[] args) {
        int i1=100;
        int i2=200;
        System.out.println(i1==i2);
        System.out.println(i1!=i2);
        System.out.println(i1>i2);
        System.out.println(i1<i2);
        System.out.println(i1>=i2);
        System.out.println(i1<=i2);

        //bitwise operator
        byte x=100;
        byte y=110;
        System.out.println(x|y);
        System.out.println(x&y);
    }
}
